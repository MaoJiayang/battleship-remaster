/**
 * AI 难度配置 v2.2 - 信息论统一框架（边际效用损失版）
 * 
 * ============================================================================
 * 核心参数说明
 * ============================================================================
 * 
 * 【alpha】探索权重 ∈ [0, 1]
 *   公式作用：utility = α × 归一化信息增益 + (1-α) × 归一化期望伤害
 *   
 *   | 取值 | 含义 | 行为特征 |
 *   |------|------|---------|
 *   | 0    | 纯伤害导向 | 只考虑期望伤害，忽略信息价值 |
 *   | 0.5  | 平衡 | 信息收集与伤害输出同等重要 |
 *   | 1    | 纯信息导向 | 只考虑信息增益，忽略伤害（声纳优先） |
 *   
 *   实际效果：
 *   - 低 alpha（< 0.3）：集火已命中区域，快速击沉
 *   - 中 alpha（0.4~0.6）：侦查与输出交替
 *   - 高 alpha（> 0.7）：广泛侦查，积累信息后集火
 * 
 * 【randomness】随机决策概率 ∈ [0, 1]
 *   公式作用：每回合以此概率放弃理性决策，改为随机攻击
 *   
 *   | 取值 | 含义 | 行为特征 |
 *   |------|------|---------|
 *   | 0    | 完全理性 | 每次都选择最优行动 |
 *   | 0.3  | 偶有失误 | 约 1/3 的回合随机攻击 |
 *   | 0.6  | 经常犯错 | 超过一半回合随机 |
 *   | 1    | 纯随机 | 完全随机攻击（最低难度） |
 *   
 *   设计目的：模拟 AI 的"失误"，降低难度
 * 
 * 【riskAwareness】风险意识强度 ∈ [0, 1]
 *   公式作用：finalScore = baseUtility × (1 + riskAwareness × normRiskBonus)
 *   
 *   | 取值 | 含义 | 效果 |
 *   |------|------|------|
 *   | 0    | 忽略风险 | finalScore = baseUtility |
 *   | 0.5  | 中等风险意识 | 风险最多让效用提升 50% |
 *   | 1    | 高风险意识 | 风险最多让效用翻倍 |
 *   
 *   核心机制（v2.2 改进）：
 *   - 使用「边际效用损失」统一计算能力价值
 *   - normRiskBonus = Σ(沉没概率 × 效用损失) / 高威胁船只数
 *   - 效用损失通过 evaluateAction 自动计算，无需硬编码权重
 *   
 *   行为效果：
 *   - 当 CV 受威胁时，AI 更倾向于使用空袭（HE）
 *   - 当 DD/SS 受威胁时，AI 更倾向于使用水听（SONAR）
 *   - 当 BB/CL/SS 受威胁时，AI 更倾向于使用高伤害主炮（AP）
 * 
 * ============================================================================
 * 难度设计理念
 * ============================================================================
 * 
 * EASY（简单）：
 *   - 高随机性，经常犯错
 *   - 偏重直接伤害，缺乏侦查
 *   - 几乎不考虑风险，不会战术性抢用能力
 * 
 * NORMAL（普通）：
 *   - 适度随机，偶有失误
 *   - 平衡探索与伤害
 *   - 轻微风险意识，偶尔会抢用濒危能力
 * 
 * HARD（困难）：
 *   - 完全理性，从不失误
 *   - 偏重信息收集，积累情报后精准打击
 *   - 较强风险意识，会主动利用即将丧失的能力
 */

